package ru.nb.neurochat.domain.model

/**
 * Ветка диалога. id = 1 у системно создаваемой главной ветки «main».
 *
 * Модель ссылочная (не копирующая): ветка не дублирует сообщения родителя, а
 * наследует их через цепочку [parentBranchId]. При чтении истории сообщения
 * предков подмешиваются автоматически.
 *
 * [forkFromMessageId] — id последнего собственного сообщения родителя на момент
 * ответвления. Это «точка отсечения»: при чтении из текущей ветки сообщения
 * родителя видны только с id <= forkFromMessageId. Сообщения, добавленные в
 * родителя после ответвления, в дочернюю ветку не попадают.
 *
 * Значения для веток с [parentBranchId] != null:
 * - > 0: обычный cutoff — сообщения родителя видны до этого id включительно.
 * - 0: родитель не имел собственных сообщений в момент fork; из него ничего не видно.
 * - null: у ветки нет предков (это root-ветка, [parentBranchId] = null).
 */
data class Branch(
    val id: Long,
    val name: String,
    val parentBranchId: Long?,
    val forkFromMessageId: Long?,
    val createdAt: Long,
)
